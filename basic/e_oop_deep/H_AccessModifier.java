package e_oop_deep;

public class H_AccessModifier {
    private int prv;
            int dft;
    protected int prt;
    public int pub;

    public static void main(String[] args) {
        /*
        접근 제어자

        private : 같은 클래스 내에서만 접근 가능
        (default) : 같은 패키지 내에서만 접근 가능
        protected : 같은 패키지 내 or 다른 패키지의 자손 클래스에서 접근 가능
        public : 접근 제한 없음

        class 앞에는 public, (default)만 붙일 수 있다.

        */
    }
}
